        <div class="portfolio-desc">
            <h3>
                NUEVA GESTIÓN DE CAME: EL BCRA EXTENDIÓ LOS PLAZOS DE PRESENTACIÓN DE
                CHEQUES Y ELIMINÓ COMISIONES
            </h3>

            <p>
                Tras el pedido que realizamos desde la Confederación Argentina de la
                Mediana Empresa (CAME), el Banco Central de la República Argentina
                (BCRA), a través de la Comunicación A 6950, extendió por 30 días
                adicionales el plazo para la presentación de los cheques comunes o de
                pago diferido que hayan sido librados en la República Argentina o en el
                exterior. Asimismo, se dispuso que las entidades financieras no podrán
                aplicar comisiones a sus clientes relacionadas con el rechazo de cheques
                y se estableció admitir una segunda presentación para estos cheques por
                causal "Sin fondos suficientes disponibles en cuenta" de acuerdo con las
                normas sobre "Sistema Nacional de Pagos - Cheques y Otros Instrumentos
                Compensables".
            </p>
