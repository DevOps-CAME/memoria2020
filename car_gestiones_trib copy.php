            <h3>
                Solicitamos la creación de un plan de pagos para los saldos a ingresar
                por Impuesto a las ganancias para sociedades, así como la prórroga de la
                presentación y determinación del saldo de Bienes Personales por acciones
                y participaciones societarias.
            </h3>

            <p>
                Solicitamos que se disponga la prórroga de los plazos de acogimiento y
                vencimiento de la primera cuota del régimen de regularización de
                obligaciones tributarias, prevista para el 31 de octubre, de acuerdo a
                lo establecido en la Ley 27.562, reglamentada mediante Resolución Gral.
                4816/20.

                Ante la inminencia del vencimiento mencionado, observamos con
                preocupación que la mayoría de los sectores comerciales e industriales
                que han retomado sus actividades verifican niveles de venta, en
                promedio, muy inferiores a los que reportaban previo a la pandemia. A
                ello se agrega que todavía hay rubros que aún no fueron habilitados para
                trabajar, y que se registran situaciones puntuales de emergencias
                agropecuarias en algunas provincias que afectan a los pequeños
                productores locales, así como a las economías en las que se insertan.

            </p>

